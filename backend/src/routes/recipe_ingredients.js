/**
 *  @swagger
 *  /api/recipe_ingredients:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Recipe_ingredients]
 *      summary: Get all recipe_ingredients
 *      description: Get all recipe_ingredients
 *      responses:
 *        200:
 *          description: Recipe_ingredients list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Recipe_ingredients"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */

router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;

    const globalAccess = req.currentUser.app_role.globalAccess;

    const payload = await Recipe_ingredientsDBApi.findAll(
      req.query,
      globalAccess,
    );
    if (filetype && filetype === 'csv') {
      const fields = ['id', 'quantity'];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

